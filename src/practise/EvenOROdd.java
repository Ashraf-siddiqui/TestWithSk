package practise;

public class EvenOROdd {

	public static boolean check(int n)
	{
		
		if(n%2==0)
		{
			return true;
		}
		
		
		return false;
	}
	
	
	
	public static void main(String[] args) {
		
		
		
		
		if(EvenOROdd.check(1))
		{
			System.out.println("number is even");
		}
		
		else {
			System.out.println("number is odd");
		}
		
		
		
		
		
		
		
	}
	
}
